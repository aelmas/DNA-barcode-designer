
public class SizePair {
		
	private int rows;
	private int cols;
	
	public SizePair(int row, int col){
		
		rows = row;
		cols = col;
	}

	public SizePair(){
			
	}

	public void setRowsize(int rows) {
		this.rows = rows;
	}
	
	public int getRowsize() {
		return rows;
	}

	public void setColsize(int cols) {
		this.cols = cols;
	}

	public int getColsize() {
		return cols;
	}

	
	
}

 
